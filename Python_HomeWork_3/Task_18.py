"""
Требуется найти в массиве A[1..N] самый близкий по величине элемент к заданному числу X.
Пользователь в первой строке вводит натуральное число N – количество элементов в массиве.
В последующих  строках записаны N целых чисел Ai. Последняя строка содержит число X
*Пример:*
5
    1 2 3 4 5
    6
    -> 5
"""

n = int(input("Введите количество элементов в массиве: "))
a = list(map(int, input("Введите элементы массива через пробел: ").split()))
x = int(input("Введите искомое число X: "))

# инициализируем минимальную разность и минимальный элемент
min_diff = abs(x - a[0])
min_elem = a[0]

# перебираем все элементы массива
for elem in a:
    # вычисляем модуль разности между элементом и искомым числом
    diff = abs(x - elem)
    # если разность меньше текущей минимальной разности,
    # то обновляем минимальную разность и минимальный элемент
    if diff < min_diff:
        min_diff = diff
        min_elem = elem

# выводим минимальный элемент
print(min_elem)




